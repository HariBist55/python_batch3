day_num = int(input("Enter the day number: "))

if day_num == 1:
    print("The day is Sunday")
elif day_num == 2:
    print("The day is Monday")
elif day_num == 3:
    print("The day is Tuesday")
elif day_num == 4:
    print("The day is Wednesday")
elif day_num == 5:
    print("The day is Thursday")
elif day_num == 6:
    print("The day is Friday")
elif day_num == 7:  
    print("The day is Saturday")
else:
    print("Invalid day number.")
    
# The code above is a simple example of a day checker. The user is asked to enter a number from 1 to 7.
# The code then checks if the number is between 1 and 7, and prints the corresponding day name.